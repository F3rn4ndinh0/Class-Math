//Math.floor()
//O método Math.floor() arredonda um número PARA BAIXO para o inteiro mais próximo e retorna o resultado. 

public class Exemplo01_Math {
    
    public static void main(String[] args) {

        System.out.println(Math.floor(2.45)); //2.0
        System.out.println(Math.floor(3.75)); //3.0
    }
}